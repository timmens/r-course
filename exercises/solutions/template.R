ackermann <- function(n, m) {
  if (m == 0) {
    return(n + 1)
  } else if (n == 0 && m > 0) {
    return(ackermann(1, m-1))
  } else {
    return(ackermann(ackermann(n-1, m), m-1))
  }
}

